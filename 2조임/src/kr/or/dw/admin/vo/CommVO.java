package kr.or.dw.admin.vo;

public class CommVO {
	private int c_no;
	private int u_no;
	private String name;
	private String c_date;
	private String w_incen;
	private String n_incen;
	private String incen;
	private String total;
	private String deduction;
	private String actual;
	public int getC_no() {
		return c_no;
	}
	public void setC_no(int c_no) {
		this.c_no = c_no;
	}
	public int getU_no() {
		return u_no;
	}
	public void setU_no(int u_no) {
		this.u_no = u_no;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getC_date() {
		return c_date;
	}
	public void setC_date(String c_date) {
		this.c_date = c_date;
	}
	public String getW_incen() {
		return w_incen;
	}
	public void setW_incen(String w_incen) {
		this.w_incen = w_incen;
	}
	public String getN_incen() {
		return n_incen;
	}
	public String getIncen() {
		return incen;
	}
	public void setIncen(String incen) {
		this.incen = incen;
	}
	public void setN_incen(String n_incen) {
		this.n_incen = n_incen;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getDeduction() {
		return deduction;
	}
	public void setDeduction(String deduction) {
		this.deduction = deduction;
	}
	public String getActual() {
		return actual;
	}
	public void setActual(String actual) {
		this.actual = actual;
	}
	
	
}
